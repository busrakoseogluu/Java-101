import java.util.Scanner;

public class VKI {

	public static void main(String[] args) {

		/*
		 * Java ile kullanýcýdan boy ve kilo deðerlerini alýp bir deðiþkene atayýn.
		 * Aþaðýdaki formüle göre kullanýcýnýn "Vücut Kitle Ýndeks" deðerini hesaplayýp
		 * ekrana yazdýrýn. Formül Kilo (kg) / Boy(m) * Boy(m)
		 */

		Scanner scan = new Scanner(System.in);

		double boy, kilo, vki;
		
		System.out.print("Boyunuzu m cinsinden giriniz : ");
		boy=scan.nextDouble();
		System.out.print("Kilonuzu giriniz : ");
        kilo=scan.nextDouble();
        
        vki=kilo/(boy*boy);
        System.out.println("Vücut kitle indeksiniz : " + vki);
		
	}

}
